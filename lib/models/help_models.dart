class HelpTopic {
  const HelpTopic({
    required this.title,
    required this.items,
  });

  final String title;
  final List<String> items;
}

class ContactChannel {
  const ContactChannel({
    required this.title,
    required this.subtitle,
    required this.action,
  });

  final String title;
  final String subtitle;
  final String action;
}

class HelpGuide {
  const HelpGuide({
    required this.title,
    required this.description,
  });

  final String title;
  final String description;
}

























